package com.zlm.vo;

public class OrderSelect {
    private int id;
    private double oprice;
    private String name;
    private String getcar;
    private String backcar;
    private String status;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public double getOprice() {
        return oprice;
    }

    public void setOprice(double oprice) {
        this.oprice = oprice;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getGetcar() {
        return getcar;
    }

    public void setGetcar(String getcar) {
        this.getcar = getcar;
    }

    public String getBackcar() {
        return backcar;
    }

    public void setBackcar(String backcar) {
        this.backcar = backcar;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
}
